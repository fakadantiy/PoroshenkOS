void write_regs(unsigned char *regs) {
	unsigned i;

	/* write MISCELLANEOUS reg */
	outb(0x3C2, *regs);
	regs++;
	/* write SEQUENCER regs */
	for(i = 0; i < 5; i++)
	{
		outb(0x3c4, i);
		outb(0x3c5, *regs);
		regs++;
	}
	/* unlock CRTC registers */
	outb(0x3d4, 0x03);
	outb(0x3d5, inb(0x3d5) | 0x80);
	outb(0x3d4, 0x11);
	outb(0x3d5, inb(0x3d5) & ~0x80);
	/* make sure they remain unlocked */
	regs[0x03] |= 0x80;
	regs[0x11] &= ~0x80;
	/* write CRTC regs */
	for(i = 0; i < 25; i++)
	{
		outb(0x3d4, i);
		outb(0x3d5, *regs);
		regs++;
	}
	/* write GRAPHICS CONTROLLER regs */
	for(i = 0; i < 9; i++)
	{
		outb(0x3ce, i);
		outb(0x3cf, *regs);
		regs++;
	}
	/* write ATTRIBUTE CONTROLLER regs */
	for(i = 0; i < 21; i++)
	{
		(void)inb(0x3da);
		outb(0x3c0, i);
		outb(0x3c0, *regs);
		regs++;
	}
	/* lock 16-color palette and unblank display */
	(void)inb(0x3da);
	outb(0x3c0, 0x20);
}

