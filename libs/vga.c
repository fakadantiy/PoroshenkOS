static inline void rgb332_to_rgb666(unsigned char rgb, unsigned char *r, unsigned char *g, unsigned char *b) {
	*r = (rgb >> 5) * 8;
	*g = ((rgb >> 2) & 0x07) * 8;
	*b = (rgb & 0x03) * 21;
}

__attribute__((cold)) void set_rgb332_palette(void) {
	outb(0x3C8, 0);

	for (int i = 0; i < 256; i++) {
		unsigned char r, g, b;
		rgb332_to_rgb666(i, &r, &g, &b);
		outb(0x3C9, r);
		outb(0x3C9, g);
		outb(0x3C9, b);
	}
}

__attribute__((cold)) void set_text_palette() {
	outb(0x3C8, 0);
	for (int i = 0; i < 16; i++) {
		outb(0x3C9, text_palette[i][0] >> 2);
		outb(0x3C9, text_palette[i][1] >> 2);
		outb(0x3C9, text_palette[i][2] >> 2);
	}
}

