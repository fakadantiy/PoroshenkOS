__attribute__((hot)) void drawchar(unsigned char c, int x, int y) {
	int cx,cy;
	int mask[8]={1,2,4,8,16,32,64,128}; // человек маска
	const unsigned char *glyph=font+(int)c*16;

	for(cy=0;cy<16;cy++) {
		for(cx=0;cx<8;cx++) {
			putpixel(glyph[cy]&mask[7-cx]?255:0,x+cx,y+cy);
		}
	}
	cx = 9;
	for(cy=0;cy<16;cy++) {
		putpixel((glyph[cy]&mask[7-cx]) && c>=176 && c <= 223 ? 255:0,x+8,y+cy); // 9-th row is 8-th
	}

}
