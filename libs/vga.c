static __attribute__((cold)) void erase_vram() {
	outb(0x3C4, 0x04); // all planes
	outb(0x3C5, 0xff);

	memset(VIDMEM, 0, 4000);
	memset(VIDMEM_G, 0, 64000);

	outb(0x3C4, 0x04); // restore
	outb(0x3C5, 0x02);
}

__attribute__((cold)) void set_graphics_mode() {
	isInGraphicsMode = true;
	write_regs(g_320x200x256);
	set_rgb332_palette();
}

__attribute__((cold)) void set_text_mode() {
	isInGraphicsMode = false;
	write_regs(g_80x25_text);
	erase_vram();
	set_text_palette();
	load_text_font();
	cursor = 0;
	clearscreen();
}

